function handleAddToCartClick(event) {
var x = parseInt(event.target.dataset.setValue); 
cartValue += x;
cartItemCount++;

var cartValueDisplay = document.getElementById('cartValueDisplay');
cartValueDisplay.textContent = `($${cartValue}) - ${cartItemCount} items`;

if (cartValue > 0) {
cartValueDisplay.classList.remove('cart-hidden');
}

event.target.textContent = 'ADDED';

var itemContainer = event.target.closest('.product-item');
var item = {
name: itemContainer.querySelector('p').textContent,  
price: itemContainer.querySelector('span p').textContent, 
img: itemContainer.querySelector('img').src,  
quantity: 1  
};

var existingItem = savedItems.find(function(savedItem) {
return savedItem.name === item.name;
});

if (existingItem) {
existingItem.quantity++;
} else {
savedItems.push(item);
}

updateSavedItems();

updateBillingDetails();
}

function handleIncreaseQuantity(event) {
var savedItemIndex = event.target.closest('.saved-item').dataset.index;  
var savedItem = savedItems[savedItemIndex];

savedItem.quantity++; 
cartValue += parseInt(savedItem.price.replace('$', ''));  
cartItemCount++;

updateSavedItems();

updateBillingDetails();
}

function handleDecreaseQuantity(event) {
var savedItemIndex = event.target.closest('.saved-item').dataset.index;  
var savedItem = savedItems[savedItemIndex];

if (savedItem.quantity > 1) {
savedItem.quantity--;  
cartValue -= parseInt(savedItem.price.replace('$', ''));  
cartItemCount--;
} else {
savedItems.splice(savedItemIndex, 1);
}

updateSavedItems();

updateBillingDetails();
}

function updateBillingDetails() {

var totalItems = 0;
var totalPrice = 0;

for (var i = 0; i < savedItems.length; i++) {
totalItems += savedItems[i].quantity; 
totalPrice += parseFloat(savedItems[i].price.replace('$', '')) * savedItems[i].quantity; 
}

var deliveryCharge = 5.00; 
var handlingCharge = 2.00; 

var grandTotal = totalPrice + deliveryCharge + handlingCharge;

document.getElementById('totalPrice').textContent = `$${totalPrice.toFixed(2)}`;
document.getElementById('deliveryCharge').textContent = `$${deliveryCharge.toFixed(2)}`;
document.getElementById('handlingCharge').textContent = `$${handlingCharge.toFixed(2)}`;
document.getElementById('grandTotal').textContent = `$${grandTotal.toFixed(2)}`;
}




