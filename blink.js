function handleSortChange() {
var selectedOption = document.getElementById('sortPrice').value;

var currentCategory = document.querySelector('#blank-container').children;
if (currentCategory.length > 0) {
var itemsContainer = currentCategory[0];
var items = Array.from(itemsContainer.children);

if (selectedOption === 'lowToHigh') {
    items.sort(function(a, b) {
        var priceA = parseFloat(a.querySelector('span p').innerText);
        var priceB = parseFloat(b.querySelector('span p').innerText);
        return priceA - priceB;
    });
} else if (selectedOption === 'highToLow') {
    items.sort(function(a, b) {
        var priceA = parseFloat(a.querySelector('span p').innerText);
        var priceB = parseFloat(b.querySelector('span p').innerText);
        return priceB - priceA;
    });
}

itemsContainer.innerHTML = '';
items.forEach(function(item) {
    itemsContainer.appendChild(item);
});
}
}

